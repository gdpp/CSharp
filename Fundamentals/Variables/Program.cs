namespace Variables
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // bool     - booleans true or false
            // char     - characters
            // int      - integer numbers
            // float    - decimal points numbers
            // string   - words, phrases

            // Declaration      - create the variable
            // Initialization   - Set the first value
            // Assignment       - Assign a value.

            // Declaration -> type name;
            int power;

            // Initialization -> name = value;
            power = 100;

            // Assignment -> name = value;
            power = 300;


            Console.WriteLine(power);
        }
    }
}