        static void Main(string[] args)
        {
            // Design Patterns
            // Every pattern describe a problem that occurs constantly
            // and also a solution for itself
            // Have four elements
            // - Name
            // - Problem -> When use the patern
            // - Solution -> elements, relations, responsibilities and collaborations
            // - Consequences -> Cost/Benefit, Space/Time, language, extension and portability

            // It has 3 categories
            // - CREATION
            // The pattern handles the creation of the objects instead of creating us.
            // * Abstract Factory
            // * Factory Method
            // * Prototype
            // * Singleton

            // - STRUCTURE
            // Helps to create groups of objects in useful structures
            // * Adapter
            // * Bridge
            // * Decorator
            // * Facade
            // * Proxy

            // - BEHAVIOR
            // Helps to define the communication between objects in the system and how is the flow control
            // * Command
            // * Interpeter
            // * Iterator
            // * Observer
            // * Strategy
        }
    }
